# max = return the maximum value from multiple elements

maximaT = max(1,3,4,5,5,64,3,34,554,33223,4,53)
print(maximaT)

maximaL = max([1,3,4,5,6,6,4,4433,44,54,33,33,34,445])
print(maximaL)


# min = returns the minimum value from multiple elements

minimaT = min(4,5,4,3,3,3,4,-1,3-4,-423,-432,56-9434)
print(minimaT)

# sum = returns the sum of list of elements

total = sum([4,5,4,4,34,43,4,3,5])
print(total)

