# Variables
age = 45  # Integer
interest_rate = 2.5  # Float
name = "Sarafat Karim"  # String
District = 'Comilla'  # String

# Printing values and types
print(age)  # 45
print(age * interest_rate)  # 112.5
print(name)  # Sarafat Karim
print(District)  # Comilla
print('Sarafat' + 'Karim')  # Concatenating strings without f-string (No formatting required)
print(f"Sarafat Karim's age is {age}, living in {District}, with an interest rate of {interest_rate}")  # Using f-string for formatting, allowing variable insertion directly into the string
print(type(age))  # <class 'int'>
print(type(interest_rate))  # <class 'float'>
print(type(name))  # <class 'str'>
